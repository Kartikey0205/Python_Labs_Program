# WAP to add and subtract two integer type data using Functions

def func(x, y):
    print("Addition of two numbers are  ", x + y)
    print("Subtraction of two numbers are  ", x - y)


a = int(input("Enter the first number "))
b = int(input("Enter the second number "))

func(a, b)
