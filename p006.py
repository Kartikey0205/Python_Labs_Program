# WAP to perform addition , subtraction , multiplication , division , floor division , modulus of float data type


a = float(input("Enter the first float type number "))
b = float(input("Enter the second float type number "))
print("The addtion of ", a, "and", b, "is ", a + b)
print("The subtraction of ", a, "and", b, "is ", a - b)
print("The multiplication of ", a, "and", b, "is ", a * b)
print("The division of ", a, "and", b, "is ", a / b)
print("The floor division of ", a, "and", b, "is ", a // b)
print("The modulus of ", a, "and", b, "is ", a % b)

